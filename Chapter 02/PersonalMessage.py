# The First Name of a Person
firstName = "scott"
# The Last Name of a Person
lastName = "lamb"
# The Full Name of a Person
fullName = "{} {}".format(firstName, lastName)
# A Message to a Person
message = "would you like to learn python with me"
# A Concatenation of the Full Name and the Message
fullMessage = "{}, {}?".format(message, fullName)
# Printing the Message
print(fullMessage.title())